import fs from 'fs'
import path from 'path'

const ROOT = path.join(process.cwd(), 'src', 'content')
const OUT = path.join(process.cwd(), 'src', 'content', 'contentIndex.ts')

function walk(dir) {
  const entries = fs.readdirSync(dir, { withFileTypes: true })
  const out = []
  for (const e of entries) {
    if (e.name.startsWith('.')) continue
    const p = path.join(dir, e.name)
    if (e.isDirectory()) out.push(...walk(p))
    else if (e.isFile() && e.name.endsWith('.json')) out.push(p)
  }
  return out
}

const files = walk(ROOT).sort()
const lines = []
lines.push('/* eslint-disable */')
lines.push('/**')
lines.push(' * AUTO-GENERATED by scripts/gen-content-index.mjs')
lines.push(' *')
lines.push(' * Why: Metro bundling does not guarantee dynamic require("./" + path) works in production.')
lines.push(' * This map makes content imports static and deterministic.')
lines.push(' */')
lines.push('')
lines.push('export const CONTENT_INDEX: Record<string, any> = {')
for (const abs of files) {
  const relFromRoot = path.relative(ROOT, abs).split(path.sep).join('/')
  const relFromOut = './' + path.relative(path.dirname(OUT), abs).split(path.sep).join('/')
  lines.push(`  ${JSON.stringify(relFromRoot)}: require(${JSON.stringify(relFromOut)}),`)
}
lines.push('}')
lines.push('')
lines.push('export function getBundledContentJson(filePath: string) {')
lines.push('  return CONTENT_INDEX[filePath] ?? null')
lines.push('}')
lines.push('')
fs.writeFileSync(OUT, lines.join('\n'))
console.log(`Generated ${path.relative(process.cwd(), OUT)} with ${files.length} entries`) 
