#!/usr/bin/env node
import fs from "node:fs"
import path from "node:path"

const inputPath = process.argv[2]
if (!inputPath) {
  console.error("Usage: npm run tokens:import:figma -- <path-to-figma-token-json>")
  process.exit(1)
}

const cwd = process.cwd()
const absInput = path.resolve(cwd, inputPath)
if (!fs.existsSync(absInput)) {
  console.error(`Input file not found: ${absInput}`)
  process.exit(1)
}

const raw = fs.readFileSync(absInput, "utf8")
let parsed
try {
  parsed = JSON.parse(raw)
} catch (error) {
  console.error(`Invalid JSON: ${(error && error.message) || String(error)}`)
  process.exit(1)
}

const payload = {
  importedAt: new Date().toISOString(),
  sourceFile: inputPath,
  tokens: parsed,
}

const outputPath = path.resolve(cwd, "src/ui/tokens/figma.generated.ts")
const banner = "// Auto-generated by scripts/import-figma-tokens.mjs. Do not edit by hand.\n"
const content = `${banner}\nexport const figmaTokenSnapshot = ${JSON.stringify(payload, null, 2)} as const\n`

fs.writeFileSync(outputPath, content, "utf8")
console.log(`Wrote ${path.relative(cwd, outputPath)}`)
